/*This program shows a possible use of type-casting*/

public class TypeCasting {
   public static void main(String args[])
      {
          float result = 10.56f;
   	
   	      System.out.println("The value stored in the variable result is " +result+
   	                         "\n\nThe whole part of this value is " + (int)result);
     	
      }
}  	